// =====================================================
// MAINSTAGE PSYTRANCE / CLASSY / 140 BPM PSY
// estilo Astrix / Ace Ventura / Sonic Species
// entre Prog Psy e Psy clássico
// 138 - 144 BPM
// =====================================================

setcpm(142)


// -----------------------------------------------------
// CONTROLES AO VIVO
// -----------------------------------------------------

// filtro lead
const energy = slider(2600, 500, 6500)

// bass brightness
const bassCut = slider(950, 120, 2400)

// reverb lead
const roomAmt = slider(0.22, 0, 1)

// intensidade geral
const lift = slider(0.85, 0.2, 1.6)


// -----------------------------------------------------
// HARMONIA
// -----------------------------------------------------
const roots = [
  "e2",
  "g2",
  "a2",
  "b2"
]

const chords = [
  "[e4,g4,b4]",
  "[g4,b4,d5]",
  "[a4,c5,e5]",
  "[b4,d5,f#5]"
]

const melody = [
  "e5 g5 b5",
  "g5 a5 d6",
  "a5 c6 e6",
  "b5 d6 f#6"
]


// =====================================================
// TRACK
// =====================================================
$: stack(

  // =================================================
  // KICK MAINSTAGE
  // =================================================
  s("bd")
    .struct("x*4")
    .gain(4.4),

  // =================================================
  // BASSLINE GROOVE
  // mais redondo que fullon
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("saw")
    .struct("- x - x - x - x")
    .decay(0.09)
    .lpf(bassCut)
    .distort(0.55)
    .gain(1.0)
    .o(2),

  // =================================================
  // SUB BASS
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("sine")
    .slow(2)
    .struct("x ~ x ~")
    .lpf(170)
    .gain(0.34)
    .o(3),

  // =================================================
  // CLAP
  // =================================================
  s("cp")
    .struct("- - x - - - x -")
    .gain(0.75)
    .room(0.12),

  // =================================================
  // CLOSED HAT
  // =================================================
  s("hh*8")
    .gain(0.26),

  // =================================================
  // OPEN HAT
  // =================================================
  s("oh")
    .struct("- - - - x - - -")
    .gain(0.22),

  // =================================================
  // RIDE
  // =================================================
  s("hh")
    .fast(4)
    .gain(0.08),

  // =================================================
  // MAIN LEAD
  // festival style
  // =================================================
  note(
    pick(
      chords,
      "{<0@2 1 2@2 3>}x2"
    )
  )
    .sound("supersaw")
    .ply(3)
    .struct("x - - x - x - -")
    .transpose("[0,12]")
    .release(0.25)
    .lpf(energy)
    .room(roomAmt)
    .gain(lift)
    .jux(rev)
    .o(4),

  // =================================================
  // MELODY LINE
  // =================================================
  note(
    pick(
      melody,
      "<0 1 2 3>"
    )
  )
    .sound("square")
    .fast(2)
    .lpf(energy)
    .room(0.35)
    .gain(0.22)
    .o(4),

  // =================================================
  // ACID TOPLINE
  // =================================================
  note("e6 g6 a6 b6")
    .sound("saw")
    .fast(4)
    .sometimes(x => x.rev())
    .sometimes(x => x.transpose(12))
    .lpf(energy)
    .gain(0.10)
    .o(5),

  // =================================================
  // FX SWEEP
  // =================================================
  s("noise")
    .slow(8)
    .lpf(energy)
    .gain(0.05),

  // =================================================
  // ATMOS PAD
  // =================================================
  note("[e4,b4] [g4,d5]")
    .sound("sine")
    .slow(4)
    .room(1)
    .gain(0.06)
    .o(6)

)


// =====================================================
// MASTER
// =====================================================
all(x =>
  x.room(0.04)
   .hpf(35)
)


