// =====================================================
// TRIBAL PSY / ETHNIC PSY / TRIPLET DROP
// estilo Juno Reactor / Vini Vici
// groove tribal + tambores + vocal chants
// 136 - 140 BPM
// =====================================================

setcpm(138)


// -----------------------------------------------------
// CONTROLES AO VIVO
// -----------------------------------------------------

// filtro lead
const energy = slider(2600, 400, 6500)

// bass
const bassCut = slider(900, 120, 2200)

// reverb percussão
const tribalRoom = slider(0.22, 0, 1)

// intensidade drop
const force = slider(0.9, 0.2, 1.8)


// -----------------------------------------------------
// ROOTS
// -----------------------------------------------------
const roots = [
  "e2",
  "g2",
  "a2",
  "b2"
]

const leadnotes = [
  "[e5,g5,b5]",
  "[g5,b5,d6]",
  "[a5,c6,e6]",
  "[b5,d6,f#6]"
]


// =====================================================
// TRACK
// =====================================================
$: stack(

  // =================================================
  // KICK MAIN
  // =================================================
  s("bd")
    .struct("x*4")
    .gain(4.5),

  // =================================================
  // TRIBAL BASS (TRIPLETS)
  // assinatura do estilo
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("saw")
    .struct("- x x - x x - x")
    .decay(0.08)
    .lpf(bassCut)
    .distort(0.6)
    .gain(1.05)
    .o(2),

  // =================================================
  // SUB BASS FUNDO
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("sine")
    .slow(2)
    .struct("x ~ x ~")
    .lpf(160)
    .gain(0.32)
    .o(3),

  // =================================================
  // CLAP
  // =================================================
  s("cp")
    .struct("- - x - - - x -")
    .gain(0.72)
    .room(0.12),

  // =================================================
  // CLOSED HATS
  // =================================================
  s("hh*8")
    .gain(0.22),

  // =================================================
  // OPEN HAT
  // =================================================
  s("oh")
    .struct("- - - - x - - -")
    .gain(0.18),

  // =================================================
  // CONGAS
  // =================================================
  s("perc")
    .struct("x - x x - x - x")
    .gain(0.32)
    .room(tribalRoom),

  // =================================================
  // TOMS ROLL
  // =================================================
  s("bd")
    .fast(2)
    .struct("- - - - x x x -")
    .gain(0.12)
    .room(0.25),

  // =================================================
  // ETHNIC CHANT FX
  // =================================================
  s("vox")
    .slow(8)
    .gain(0.08)
    .room(1),

  // =================================================
  // MAIN LEAD
  // =================================================
  note(
    pick(
      leadnotes,
      "{<0@2 1 2@2 3>}x2"
    )
  )
    .sound("supersaw")
    .ply(2)
    .struct("x - - x - x - -")
    .transpose("[0,12]")
    .release(0.22)
    .lpf(energy)
    .room(0.18)
    .gain(force)
    .jux(rev)
    .o(4),

  // =================================================
  // TRIBAL FLUTE STYLE TOP
  // =================================================
  note("e6 g6 a6 b6")
    .sound("square")
    .fast(3)
    .sometimes(x => x.rev())
    .lpf(energy)
    .gain(0.14)
    .room(0.45)
    .o(5),

  // =================================================
  // DROP TRIPLET STAB
  // =================================================
  note("e5 e5 g5")
    .sound("saw")
    .fast(3)
    .struct("x x x")
    .gain(0.12)
    .o(5),

  // =================================================
  // ATMOS
  // =================================================
  s("noise")
    .slow(8)
    .lpf(energy)
    .gain(0.04)

)


// =====================================================
// MASTER
// =====================================================
all(x =>
  x.room(0.03)
   .hpf(35)
)


