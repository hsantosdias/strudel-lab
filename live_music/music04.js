// =====================================================
// UK PSY / CLASSIC PSYCHEDELIC / UPTEMPO PSY
// inspirado no estilo Tristan / Ajja / Render
// linhas de baixo pulsantes + synth futurista FM
// 145 - 155 BPM
// =====================================================

setcpm(148)


// -----------------------------------------------------
// CONTROLES AO VIVO
// -----------------------------------------------------

// filtro principal dos leads
const energy = slider(3200, 500, 7000)

// brilho do bass
const bassCut = slider(1100, 150, 2500)

// reverb
const roomAmt = slider(0.18, 0, 1)

// drive geral
const drive = slider(1.0, 0.2, 2)


// -----------------------------------------------------
// ESCALA / ROOTS
// -----------------------------------------------------
const roots = [
  "f#2",
  "a2",
  "b2",
  "c#3"
]

const leadSeq = [
  "[f#5,a5,c#6]",
  "[a5,c#6,e6]",
  "[b5,d6,f#6]",
  "[c#6,e6,g#6]"
]


// =====================================================
// TRACK
// =====================================================
$: stack(

  // =================================================
  // KICK DRY / FORWARD
  // =================================================
  s("bd")
    .struct("x*4")
    .gain(4.6),

  // =================================================
  // PULSING PSY BASS
  // assinatura clássica
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("saw")
    .struct("- x - x - x - x")
    .decay(0.07)
    .attack(0.001)
    .lpf(bassCut)
    .distort(drive)
    .gain(1.2)
    .o(2),

  // =================================================
  // SUB FUNDÃO AO FUNDO
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("sine")
    .struct("x ~ x ~")
    .slow(2)
    .lpf(160)
    .gain(0.38)
    .o(3),

  // =================================================
  // MID BASS CLICKY
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("square")
    .struct("- x - x - x - x")
    .decay(0.03)
    .distort(1.6)
    .lpf(850)
    .gain(0.22)
    .o(2),

  // =================================================
  // CLOSED HATS
  // =================================================
  s("hh*8")
    .gain(0.32),

  // =================================================
  // OPEN HAT
  // =================================================
  s("oh")
    .struct("- - - - x - - -")
    .gain(0.25),

  // =================================================
  // CLAP
  // =================================================
  s("cp")
    .struct("- - x - - - x -")
    .gain(0.65)
    .room(0.12),

  // =================================================
  // FM FUTURISTIC LEAD
  // =================================================
  note(
    pick(
      leadSeq,
      "{<0@2 1 2@2 3>}x2"
    )
  )
    .sound("fm")
    .ply(2)
    .struct("x - - x - x - -")
    .transpose("[0,12]")
    .lpf(energy)
    .release(0.18)
    .room(roomAmt)
    .gain(0.55)
    .jux(rev)
    .o(4),

  // =================================================
  // ACID LINE
  // =================================================
  note("f#5 a5 c#6 e6")
    .sound("saw")
    .fast(4)
    .sometimes(x => x.rev())
    .sometimes(x => x.transpose(12))
    .lpf(energy)
    .gain(0.18)
    .room(0.35)
    .o(4),

  // =================================================
  // LASER FX
  // =================================================
  note("c#6 f#6 a6")
    .sound("square")
    .fast(8)
    .rarely(x => x.rev())
    .gain(0.08)
    .lpf(energy)
    .o(5),

  // =================================================
  // ATMOS / NOISE
  // =================================================
  s("noise")
    .slow(8)
    .lpf(energy)
    .gain(0.04),

  // =================================================
  // MICRO SHAKER
  // =================================================
  s("hh")
    .fast(16)
    .gain(0.03)
    .room(1)

)


// =====================================================
// MASTER
// =====================================================
all(x =>
  x.room(0.03)
   .hpf(32)
)


