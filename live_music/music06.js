// =====================================================
// RAW TRANCE / NEOTRANCE / ETHEREAL TRANCE
// inspirado em estética 90s underground
// 303 + 909 + vibe hipnótica / crua / analógica
// 130 - 160 BPM
// =====================================================

setcpm(142)


// -----------------------------------------------------
// CONTROLES AO VIVO
// -----------------------------------------------------

// cutoff da 303
const acid = slider(1800, 200, 6000)

// respiro geral
const roomAmt = slider(0.12, 0, 1)

// sub bass
const bassCut = slider(700, 100, 1800)

// intensidade do drive
const dirt = slider(1.2, 0.2, 3)


// -----------------------------------------------------
// NOTAS
// -----------------------------------------------------
const roots = [
  "e2",
  "g2",
  "d2",
  "a2"
]

const stabs = [
  "[e4,b4]",
  "[g4,d5]",
  "[d4,a4]",
  "[a4,e5]"
]


// =====================================================
// TRACK
// =====================================================
$: stack(

  // =================================================
  // 909 KICK
  // =================================================
  s("bd")
    .struct("x*4")
    .gain(4.6),

  // =================================================
  // 909 CLAP
  // =================================================
  s("cp")
    .struct("- - x - - - x -")
    .gain(0.85)
    .room(0.08),

  // =================================================
  // CLOSED HAT
  // =================================================
  s("hh*8")
    .gain(0.22),

  // =================================================
  // OPEN HAT
  // =================================================
  s("oh")
    .struct("- - - - x - - -")
    .gain(0.18),

  // =================================================
  // RAW OFFBEAT BASS
  // seco e simples
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("saw")
    .struct("- x - x - x - x")
    .decay(0.08)
    .lpf(bassCut)
    .distort(0.55)
    .gain(0.95)
    .o(2),

  // =================================================
  // SUB FUNDO
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("sine")
    .slow(2)
    .struct("x ~ x ~")
    .gain(0.28)
    .lpf(160)
    .o(3),

  // =================================================
  // TB303 ACID LINE
  // protagonista
  // =================================================
  note("e5 g5 b5 d6 a5 g5")
    .sound("saw")
    .fast(2)
    .sometimes(x => x.rev())
    .sometimes(x => x.transpose(12))
    .decay(0.05)
    .distort(dirt)
    .lpf(acid)
    .gain(0.42)
    .o(4),

  // =================================================
  // SECOND ACID RANDOM
  // =================================================
  note("e6 g6 a6 b6")
    .sound("square")
    .fast(4)
    .rarely(x => x.rev())
    .rarely(x => x.fast(2))
    .lpf(acid)
    .gain(0.12)
    .o(5),

  // =================================================
  // ETHEREAL STABS
  // trance 90s pads
  // =================================================
  note(
    pick(stabs, "<0 1 2 3>")
  )
    .sound("supersaw")
    .slow(2)
    .struct("x - - - x - - -")
    .release(0.5)
    .room(roomAmt)
    .lpf(2400)
    .gain(0.20)
    .jux(rev)
    .o(6),

  // =================================================
  // AIR PAD
  // =================================================
  note("[e5,b5]")
    .sound("sine")
    .slow(8)
    .room(1)
    .gain(0.05)
    .o(7),

  // =================================================
  // NOISE RISER
  // =================================================
  s("noise")
    .slow(8)
    .lpf(acid)
    .gain(0.03),

  // =================================================
  // TAPE TEXTURE
  // =================================================
  s("hh")
    .fast(16)
    .gain(0.02)
    .room(1)

)


// =====================================================
// MASTER
// =====================================================
all(x =>
  x.room(0.03)
   .hpf(28)
)


