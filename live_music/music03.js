// =====================================================
// STRUDEL LAB - FULLON PSYTRANCE ORIGINAL
// criado para você
// bass rolando ao fundo + groove fullon
// =====================================================

// velocidade fullon
setcpm(145)


// -----------------------------------------------------
// CONTROLES AO VIVO
// -----------------------------------------------------

// filtro geral
const energy = slider(2800, 500, 6000)

// filtro do bass
const bassCut = slider(900, 100, 2200)

// reverb geral
const roomAmt = slider(0.18, 0, 1)


// -----------------------------------------------------
// NOTAS PRINCIPAIS
// -----------------------------------------------------
const roots = [
  "e2",
  "g2",
  "a2",
  "b2"
]

const leadnotes = [
  "[e4,g4,b4]",
  "[g4,b4,d5]",
  "[a4,c5,e5]",
  "[b4,d5,f#5]"
]


// =====================================================
// TRACK
// =====================================================
$: stack(

  // =================================================
  // KICK FULLON
  // =================================================
  s("bd")
    .struct("x*4")
    .gain(4.5),

  // =================================================
  // OFFBEAT BASS (marca do psy/fullon)
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("saw")
    .struct("- x - x - x - x")
    .decay(0.08)
    .lpf(bassCut)
    .distort(0.8)
    .gain(1.1)
    .o(2),

  // =================================================
  // SUB BASS FUNDO
  // grave constante ao fundo
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("sine")
    .slow(2)
    .struct("x ~ x ~")
    .lpf(180)
    .gain(0.42)
    .o(3),

  // =================================================
  // FULLON MID BASS DRIVE
  // =================================================
  note(
    pick(roots, "<0 1 2 3>")
  )
    .sound("square")
    .struct("- x - x - x - x")
    .decay(0.05)
    .distort(1.4)
    .lpf(700)
    .gain(0.35)
    .o(2),

  // =================================================
  // CLOSED HATS
  // =================================================
  s("hh*8")
    .gain(0.38),

  // =================================================
  // OPEN HAT
  // =================================================
  s("oh")
    .struct("- - - - x - - -")
    .gain(0.28),

  // =================================================
  // SNARE / CLAP
  // =================================================
  s("cp")
    .struct("- - x - - - x -")
    .gain(0.75)
    .room(0.2),

  // =================================================
  // MAIN LEAD FULLON
  // =================================================
  note(
    pick(
      leadnotes,
      "{<0@2 1 2@2 3>}x2"
    )
  )
    .sound("supersaw")
    .ply(3)
    .struct("x - - x - x - -")
    .transpose("[0,12]")
    .lpf(energy)
    .release(0.2)
    .gain(0.55)
    .room(roomAmt)
    .jux(rev)
    .o(4),

  // =================================================
  // PSY ARP
  // =================================================
  note("e5 g5 b5 d6")
    .sound("square")
    .fast(4)
    .sometimes(x => x.rev())
    .lpf(energy)
    .gain(0.22)
    .room(0.45)
    .o(4),

  // =================================================
  // FX NOISE SWEEP
  // =================================================
  s("noise")
    .slow(8)
    .lpf(energy)
    .gain(0.05),

  // =================================================
  // MICRO TEXTURE
  // =================================================
  s("hh")
    .fast(16)
    .gain(0.04)
    .room(1)

)


// =====================================================
// MASTER FX
// =====================================================
all(x =>
  x.room(0.04)
   .hpf(35)
)


